import java.util.*;;
public class Typecasting {
    public static void main(String[] args) {
  

      // Type casting

      // Type casting is nothing but compressing a bigger data type into a smaller data type 

      // for example assigning a float to a  int



      int r = (int)12.12f;

      System.out.println(r);

      //output

      /*
       * 12
       */
      //this just gives the int part of the float...



    

    }
    
}
