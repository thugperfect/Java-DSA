public class datatype {
    public static void main(String[] args) {
            
    //primitives and non primitive data type

    /* 
     * primitive :
                primitive are nothing but the data which cannot be split into further
                example: int a = 10;,char b = "a";
                here the input cannot be split into further...

      * non-primitive :
                input data which can be split into further belongs do non primimtive data type

                example: String s = "Hello";
                here the contents of the string can be split into 'H','e','l','l','o'
     */
    //primitive data types and syntax

    int a = 10; // 4 bytes
    char b = 'c';
    float c = 12.12f; // 4 bytes  ** 'f' is added at the end because by default decimal numbers are of type double
                        // to specify it is a float 'f' is added at the end

    double d = 1212.12131314; //8 bytes
    long e = 1234567890L; //8 bytes ** 'l' || 'L' like float the default data type of numbers is int
                        // to specify it a long a "l" || "L" is used...

    boolean f = true;

    }
    
}
